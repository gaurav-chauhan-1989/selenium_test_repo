numbers=[3,55,7,6,78,908,43,55,65,23,45,2]
print(numbers)
numbers.sort()                                     # Sort the numbers in increasing
print(numbers)
numbers.reverse()                                  # Reverse the list
print(numbers)
numbers.insert(2,999)                              # Insert 999 in 3rd index
print(numbers)
numbers.append(41)                                 # Insert 41 at the end of the list
print(numbers)
numbers.remove(41)                                 # Remove 41 wherever it is present in the list
print(numbers)
numbers.pop()                                     # Remove number at the end of the list
print(numbers)
del(numbers[1])
print(max(numbers))                                # Print maximum number on list
print(min(numbers))                                # Print minimum number on list

